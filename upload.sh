#!/bin/bash
# 小红书视频批量上传到 YouTube
# 用法: ./upload.sh [数量]
# 示例: ./upload.sh 5  # 上传5个视频

# 默认上传数量
LIMIT=${1:-5}

echo "=========================================="
echo "🚀 开始批量上传 ${LIMIT} 个视频"
echo "=========================================="

# 执行批量上传（默认开启翻译，跳过已上传）
python3 -m src.cli batch --limit ${LIMIT}

echo "=========================================="
echo "✅ 上传任务结束"
echo "=========================================="
